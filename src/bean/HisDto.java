package bean;

public class HisDto {
	private String pat_name;
	private String doc_name;
	private String his_date;
	private String pat_history;
	public String getPat_name() {
		return pat_name;
	}
	public void setPat_name(String pat_name) {
		this.pat_name = pat_name;
	}

	public String getDoc_name() {
		return doc_name;
	}
	public void setDoc_name(String doc_name) {
		this.doc_name = doc_name;
	}
	public String getHis_date() {
		return his_date;
	}
	public void setHis_date(String his_date) {
		this.his_date = his_date;
	}
	public String getPat_history() {
		return pat_history;
	}
	public void setPat_history(String pat_history) {
		this.pat_history = pat_history;
	}
	
}
